      SUBROUTINE area_qpts(myrank,p,ctp,nel_type,nqpta,mnqpta,wpta,qpta)

      IMPLICIT NONE 
      
      INTEGER, INTENT(IN) :: myrank
      INTEGER, INTENT(IN) :: p
      INTEGER, INTENT(IN) :: ctp
      INTEGER, INTENT(IN) :: nel_type
      INTEGER, DIMENSION(:), INTENT(OUT) :: nqpta
      INTEGER, INTENT(OUT) :: mnqpta
      REAL(rp), DIMENSION(:,:), ALLOCATABLE, INTENT(OUT) :: wpta
      REAL(rp), DIMENSION(:,:,:), ALLOCATABLE, INTENT(OUT) :: qpta
      
      INTEGER :: i,pt,et
      INTEGER :: order(nel_type)
      INTEGER :: npt(nel_type)
      REAL(rp) :: r(13**2,3,nel_type)
      
      order(1) = 2*p
      order(2) = order(1)
      order(3) = 2*(p+ctp)
      order(4) = order(3)
      

      DO i = 1,nel_type
      
        IF (mod(i,2) == 1) THEN        
          CALL tri_cubature(order(i),npt(i),r(:,:,i))
        ELSE IF (mod(i,2) == 0) THEN        
          CALL quad_cubature(order(i),npt(i),r(:,:,i))
        ENDIF
        
      ENDDO
      
      
      mnqpta = maxval(npt)
      
      
      ALLOCATE(qpta(mnqpta,2,nel_type))
      ALLOCATE(wpta(mnqpta,nel_type))
      
      DO i = 1,nel_type
        nqpta(i) = npt(i)
        DO pt = 1,mnqpta
          qpta(pt,1,i) = r(pt,1,i)
          qpta(pt,2,i) = r(pt,2,i)
          wpta(pt,i) = r(pt,3,i)          
        ENDDO
      ENDDO          


      IF (myrank == 0) THEN
        PRINT "(A)", "---------------------------------------------"
        PRINT "(A)", "        Area Integration Information         "
        PRINT "(A)", "---------------------------------------------"
        PRINT "(A)", " "

!         PRINT "(A)", 'Area quadrature weights and points (r,s)'
      
        DO i = 1,4
        PRINT "(A,I3)", "Number of area quadrature points",nqpta(i)      
!           DO pt = 1,nqpta(i)
!             PRINT "(3(F10.3))", wpta(pt,i),qpta(pt,1,i),qpta(pt,2,i)
!           ENDDO
!           PRINT*, ' '
        ENDDO      
        PRINT*, " "
      ENDIF

      
      RETURN
      END SUBROUTINE area_qpts
      
