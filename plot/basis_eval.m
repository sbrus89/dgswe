function phi = basis_eval(r,s)

    phi = zeros(length(r),15);

    phi(:,1) = 1;
    phi(:,2) = .5*(1+3*s);
    phi(:,3) = .5*(1+2*r+s);
    phi(:,4) = -.5 + s + .5*(5*s.^2);
    phi(:,5) = .25*(1 + 2*r + s).*(3 + 5*s);
    phi(:,6) = .25*(1 + 6*r.^2 + 4*s + s.^2 + 6*r.*(1 + s));
    phi(:,7) = .125*(-3 - 15*s + 15*s.^2 + 35*s.^3);
    phi(:,8) = .125*(1 + 2*r + s).*(1 + 18*s + 21*s.^2);
    phi(:,9) = .125*(5 + 7*s).*(1 + 6*r.^2 + 4*s + s.^2 + 6*r.*(1 + s));
    phi(:,10) = .125*(1 + 20*r.^3 + 9*s + 9*s.^2 + s.^3 + 30*r.^2.*(1 + s) + 12*r.*(1 + 3*s + s.^2));
    phi(:,11) = (1/8)*(3 - 12*s - 42*s.^2 + 28*s.^3 + 63*s.^4);
    phi(:,12) = (1/4)*(1 + 2*r + s).*(-2 + 21*s.^2 + 21*s.^3);
    phi(:,13) = (1/4)*(2 + 10*s + 9*s.^2).*(1 + 6*r.^2 + 4*s + s.^2 + 6*r.*(1 + s));
    phi(:,14) = (1/16)*(7 + 9*s).*(1 + 20*r.^3 + 9*s + 9*s.^2 + s.^3 + 30*r.^2.*(1 + s) + 12*r.*(1 + 3*s + s.^2));
    phi(:,15) = (1/16)*(1 + 70*r.^4 + 16*s + 36*s.^2 + 16*s.^3 + s.^4 + 140*r.^3.*(1 + s) + 30*r.^2.*(3 + 8*s + 3*s.^2) + 20*r.*(1 + 6*s + 6*s.^2 + s.^3));
    
end